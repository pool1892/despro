/*
 * Definieren Sie ein Prolog-Praedikat replicate/3, das
 * der Haskell-Funktion mit gleichem Namen entspricht, ausser
 * dass das numerische Argument symbolisch ausgedrueckt ist.
 *
 * Es soll also z.B. replicate(s(s(s(0))),a,[a,a,a]) gelten.
 */


replicate(0,_,[]).
replicate(s(A),X, [X|T]) :- replicate(A, X, T).
